# Create a program that asks the user for a number of miles and converts it to kilometers and prints the results
# conversion rate:  mile = 1.609344 Km

# start program
print("This program is to convert miles into kilometers")

#Get number of miles from user
miles = float (input("Miles:"))

#Set kilometers variable.
kilometers = 1.609344

#Convert miles into kilometers
print(miles,"Miles is equal to",miles * kilometers,"kilometers")

